package main;

import java.io.*;

/**
 * <h2><i> Methods Definition </i></h2>
 * This program implements all the methods
 * like addition, multiplication,subtraction and division two given number
 * 
 */

public class annotations 
{
	/**
	 * <h4>Addition Method</h4>
	 * This method is used to add two integer number
	 * @param num1 This is the 1st parameter for  Addition Method
	 * @param num2 This is the 2nd parameter for  Addition Method
	 * @return returns sum of num1 and num2
	 */
	public int addition(int num1, int num2)
	{
		return num1+num2;
	}


	/**
	 * <h4>Subtraction Method</h4>
	 * This method is used to subtract two integer number
	 * @param num1 This is the 1st parameter for Subtraction Method
	 * @param num2 This is the 2nd parameter for Subtraction Method
	 * @return returns subtract from num1 to num2
	 */
	public int subtraction(int num1, int num2)
	{
		return num1-num2;
	}
	
	/**
	 * <h4>Multiplication Method</h4>
	 * This method is used to multiply two integer number
	 * @param num1 This is the 1st parameter for Multiplication Method
	 * @param num2 This is the 2nd parameter for Multiplication Method
	 * @return returns multiply of num1 and num2
	 */
	public int multiplication(int num1, int num2)
	{
		return num1*num2;
	}

	/**
	 * <h4>Division Method</h4>
	 * This method is used to multiply two integer number
	 * @param num1 This is the 1st parameter for Division Method
	 * @param num2 This is the 2nd parameter for Division Method
	 * @return returns divided num1 by num2
	 */
	
	public int Division(int num1, int num2)
	{
		return num1/num2;
	}

}
